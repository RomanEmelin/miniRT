#include "../minirt.h"

int		ft_atoi(const char *str)
{
	int	n;
	int	res;

	n = 1;
	res = 0;
	while (ft_isspace(*str))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str++ == '-')
			n = -1;
	}
	while ('0' <= *str && *str <= '9')
		res = (res * 10) + (*str++ - '0');
	return (res * n);
}
